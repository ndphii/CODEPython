#sử dụng import math 
#muốn sử dụng một hàm nào đó của thư viện ta khai báo <tên thư viện>.<tên hàm> có 6 hàm cơ bản trong tính toán ... 6 ví dụ:
import math
print(math.trunc(3.34342342342343))#trả về số nguyên 
print(math.floor(-1.234))#làm tròn đến nhở hơn hoặc bằng 
print(math.ceil(10%4+3.14))#làm tròn đến số lớn hơn hoặc bằng
print(math.fabs(-41))#trị tuyệt đối
print(math.sqrt(16))#căn
print(math.gcd(2,6))#ước chung lớn nhất
